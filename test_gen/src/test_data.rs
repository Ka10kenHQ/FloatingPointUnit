/// Returns min and max values for floating point numbers as pairs (f32, f64)
pub fn min_max_values() -> Vec<(f32, f64)> {
    vec![
        (f32::MAX, f64::MAX),
        (-f32::MAX, -f64::MAX),
        (f32::MIN, f64::MIN),
        (-f32::MIN, -f64::MIN),
    ]
}
