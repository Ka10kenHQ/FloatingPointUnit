/// Returns a zero value as a floating point pair (f32, f64)
pub fn zero() -> Vec<(f32, f64)> {
    vec![(0.0_f32, 0.0_f64)]
}

/// Returns infinity values as floating point pairs (f32, f64)
pub fn infinity() -> Vec<(f32, f64)> {
    vec![
        (f32::INFINITY, f64::INFINITY),
        (-f32::INFINITY, -f64::INFINITY),
    ]
}

